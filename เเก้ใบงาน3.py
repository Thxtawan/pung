#งานคำนวณองศา
# รับค่าอุณหภูมิหน่วยองศาเซลเซียสจากผู้ใช้
celsius = float(input("กรุณาใส่อุณหภูมิ (องศาเซลเซียส): "))

# คำนวณค่าองศาฟาเรนไฮต์และเคลวิน
fahrenheit = (9 / 5) * celsius + 32
kelvin = celsius + 273.15

# แสดงผลลัพธ์
print("ผลการแปลงอุณหภูมิ:")
print(f"- องศาเซลเซียส: {celsius:.2f} °C")
print(f"- ฟาเรนไฮต์: {fahrenheit:.2f} °F")
print(f"- เคลวิน: {kelvin:.2f} K")



#งานคิดเงิน
products = {
    "หมูสับกิโล": 150.00,
    "เนื้ออกไก่": 105.00,
    "ไก่บ้าน": 120.00,
    "มาม่าต้มยำ": 6.50,
    "ข้าวสาร": 150.00,
    "น้ำตาล": 20.00,
    "ปลากระป๋องสามแม่ครัว": 10.00,
    "ซอสน้ำมันหอย": 18.00,
    "ผงชูรส": 10.25,
    "ไข่แพงคละเบอร์": 120.25,
    "ชาเขียว": 21.50,
    "Pepsi": 29.50,
    "กาแฟ": 15.75,
    "ขนมปังอบเนย": 19.00,
    "ชาไทย": 11.50,
    "น้ำเปล่า": 15.15,
    "น้ำแข็ง": 10.00
}

# งบประมาณเริ่มต้น
budget = 1000.0
total_cost = 0.0
cart = {}

# ตัวอย่าง: เลือกสินค้าและจำนวน
# (สามารถเปลี่ยนค่าตามที่ต้องการ)
selected_items = {
    "ข้าวสาร": 1,
    "มาม่าต้มยำ": 3,
    "ไข่เป่าบ้า": 2,
    "ปลากระป๋องสามแม่ครัว": 2,
    "Pepsi": 2,
    "กาแฟ": 1,
    "ขนมปังแผ่นขาว": 1,
    "น้ำเปล่า": 2,
    "น้ำแข็ง": 1
}

# คำนวณราคารวม
for item, qty in selected_items.items():
    if item in products:
        cost = products[item] * qty
        cart[item] = (qty, cost)
        total_cost += cost

# คำนวณเงินที่เหลือ
remaining = budget - total_cost

# แสดงผลลัพธ์
print("รายการที่ซื้อ:")
for item, (qty, cost) in cart.items():
    print(f"- {item} จำนวน {qty} ชิ้น/หน่วย รวม {cost:.2f} บาท")

print(f"\nใช้เงินทั้งหมด: {total_cost:.2f} บาท")
print(f"เงินที่เหลือ: {remaining:.2f} บาท")




#งานท้ายบท
# กำหนดเซต a และ b
a = {10, 20, 30, 40, 50, 60}
b = {30, 40, 50, 60, 70, 80}

# 1. การรวมเซต (Union)
print("Union:", a | b)  # หรือ a.union(b)

# 2. ค่าที่อยู่ร่วมกัน (Intersection)
print("Intersection:", a & b)  # หรือ a.intersection(b)

# 3. ค่าที่อยู่ใน a แต่ไม่อยู่ใน b (Difference)
print("Difference (a - b):", a - b)

# 4. ค่าที่ไม่ซ้ำกันในทั้งสองเซต (Symmetric Difference)
print("Symmetric Difference:", a ^ b)  # หรือ a.symmetric_difference(b)



#งานในชั้นเรียน
base = float(input())

# รับค่าความสูงจากผู้ใช้
height = float(input())

# คำนวณพื้นที่สามเหลี่ยม
area = 0.5 * base * height

